package com.linkedin.venice.benchmark.lean;

import com.linkedin.venice.exceptions.VeniceException;
import com.linkedin.venice.meta.ReadOnlySchemaRepository;
import com.linkedin.venice.schema.GeneratedSchemaID;
import com.linkedin.venice.schema.SchemaEntry;
import com.linkedin.venice.schema.rmd.RmdSchemaEntry;
import com.linkedin.venice.schema.writecompute.DerivedSchemaEntry;
import java.util.Collection;
import java.util.Collections;
import java.util.HashMap;
import java.util.Map;
import org.apache.avro.Schema;


/**
 * Lean in-memory implementation of {@link ReadOnlySchemaRepository} for the Active-Active ingestion harness.
 *
 * <p>Pre-loaded with exactly one store's schemas:
 * <ul>
 *   <li>One key schema</li>
 *   <li>One value schema (id = 1) — the {@code BenchmarkRecord} schema from
 *       {@code com.linkedin.venice.benchmark.ActiveActiveIngestionBenchmark}</li>
 *   <li>One RMD (replication-metadata) schema generated by
 *       {@link com.linkedin.venice.schema.rmd.v1.RmdSchemaGeneratorV1}, paired with the value schema id and
 *       protocolVersion = 1</li>
 *   <li>One write-compute (derived) schema generated by
 *       {@link com.linkedin.venice.schema.writecompute.WriteComputeSchemaConverter}, paired with the value schema
 *       id and protocolVersion = 1</li>
 * </ul>
 *
 * <p>Lookup methods that target the configured {@code storeName} return the pre-loaded entries; lookups for
 * other stores throw {@link VeniceException} so silent miss-as-null bugs are caught loudly during ingestion.
 *
 * <p>This is the Phase 1 deliverable of the Lean AA Ingestion Harness project; see
 * {@code autoresearch/lean-aa-harness/GOAL.md} (entry #6 of {@code dep-graph.md}).
 */
public class InMemoryReadOnlySchemaRepository implements ReadOnlySchemaRepository {
  /** Fixed value schema id for the harness — there is only ever one value schema. */
  public static final int VALUE_SCHEMA_ID = 1;
  /** RMD schema protocol version — this harness always uses {@code RmdSchemaGeneratorV1}. */
  public static final int RMD_PROTOCOL_VERSION = 1;
  /** Write-compute / derived schema protocol version — single derived schema per value schema. */
  public static final int WRITE_COMPUTE_PROTOCOL_VERSION = 1;

  private final String storeName;
  private final SchemaEntry keySchemaEntry;
  private final SchemaEntry valueSchemaEntry;
  private final RmdSchemaEntry rmdSchemaEntry;
  private final DerivedSchemaEntry writeComputeSchemaEntry;

  // Cached canonical strings for quick id lookup.
  private final Map<String, Integer> valueSchemaCanonicalToId;
  private final Map<String, GeneratedSchemaID> derivedSchemaCanonicalToId;

  /**
   * Construct the repository. All four schemas are pre-parsed and indexed eagerly.
   *
   * @param storeName the single store name this repository serves; lookups for any other store throw.
   * @param keySchema the key schema (typically simple {@code "string"}).
   * @param valueSchema the value schema (the {@code BenchmarkRecord} schema).
   * @param rmdSchema the RMD schema produced by {@code RmdSchemaGeneratorV1.generateMetadataSchema(valueSchema)}.
   * @param writeComputeSchema the write-compute schema produced by
   *        {@code WriteComputeSchemaConverter.getInstance().convertFromValueRecordSchema(valueSchema)}.
   */
  public InMemoryReadOnlySchemaRepository(
      String storeName,
      Schema keySchema,
      Schema valueSchema,
      Schema rmdSchema,
      Schema writeComputeSchema) {
    if (storeName == null || keySchema == null || valueSchema == null || rmdSchema == null
        || writeComputeSchema == null) {
      throw new IllegalArgumentException("All InMemoryReadOnlySchemaRepository constructor arguments must be non-null");
    }
    this.storeName = storeName;
    this.keySchemaEntry = new SchemaEntry(VALUE_SCHEMA_ID, keySchema);
    this.valueSchemaEntry = new SchemaEntry(VALUE_SCHEMA_ID, valueSchema);
    this.rmdSchemaEntry = new RmdSchemaEntry(VALUE_SCHEMA_ID, RMD_PROTOCOL_VERSION, rmdSchema);
    this.writeComputeSchemaEntry =
        new DerivedSchemaEntry(VALUE_SCHEMA_ID, WRITE_COMPUTE_PROTOCOL_VERSION, writeComputeSchema);

    this.valueSchemaCanonicalToId = new HashMap<>();
    this.valueSchemaCanonicalToId.put(valueSchema.toString(false), VALUE_SCHEMA_ID);
    this.derivedSchemaCanonicalToId = new HashMap<>();
    this.derivedSchemaCanonicalToId
        .put(writeComputeSchema.toString(false), new GeneratedSchemaID(VALUE_SCHEMA_ID, WRITE_COMPUTE_PROTOCOL_VERSION));
  }

  private void requireKnownStore(String requestedStore) {
    if (!storeName.equals(requestedStore)) {
      throw new VeniceException(
          "InMemoryReadOnlySchemaRepository only serves store '" + storeName + "', got: " + requestedStore);
    }
  }

  @Override
  public SchemaEntry getKeySchema(String storeName) {
    requireKnownStore(storeName);
    return keySchemaEntry;
  }

  @Override
  public SchemaEntry getValueSchema(String storeName, int id) {
    requireKnownStore(storeName);
    if (id != VALUE_SCHEMA_ID) {
      // Returning null is the documented "not found" contract for this method per other implementations
      // (e.g., HelixReadOnlySchemaRepository), so the caller can decide how to handle it.
      return null;
    }
    return valueSchemaEntry;
  }

  @Override
  public boolean hasValueSchema(String storeName, int id) {
    requireKnownStore(storeName);
    return id == VALUE_SCHEMA_ID;
  }

  @Override
  public int getValueSchemaId(String storeName, String valueSchemaStr) {
    requireKnownStore(storeName);
    Integer id = valueSchemaCanonicalToId.get(valueSchemaStr);
    if (id == null) {
      // Try canonicalising via fresh parse so the caller can pass any-format string.
      Schema parsed = new Schema.Parser().parse(valueSchemaStr);
      id = valueSchemaCanonicalToId.get(parsed.toString(false));
    }
    if (id == null) {
      throw new VeniceException(
          "Value schema not registered in InMemoryReadOnlySchemaRepository for store: " + storeName);
    }
    return id;
  }

  @Override
  public Collection<SchemaEntry> getValueSchemas(String storeName) {
    requireKnownStore(storeName);
    return Collections.singletonList(valueSchemaEntry);
  }

  @Override
  public SchemaEntry getSupersetOrLatestValueSchema(String storeName) {
    requireKnownStore(storeName);
    return valueSchemaEntry;
  }

  @Override
  public SchemaEntry getSupersetSchema(String storeName) {
    requireKnownStore(storeName);
    // For a single-value-schema store, the value schema IS the superset (there is nothing else to
    // merge). The AA merge path (MergeConflictResolver#update) requires a non-null superset value
    // schema, so returning the lone value schema here makes write-compute updates work end-to-end.
    return valueSchemaEntry;
  }

  @Override
  public GeneratedSchemaID getDerivedSchemaId(String storeName, String derivedSchemaStr) {
    requireKnownStore(storeName);
    GeneratedSchemaID id = derivedSchemaCanonicalToId.get(derivedSchemaStr);
    if (id == null) {
      Schema parsed = new Schema.Parser().parse(derivedSchemaStr);
      id = derivedSchemaCanonicalToId.get(parsed.toString(false));
    }
    return id == null ? GeneratedSchemaID.INVALID : id;
  }

  @Override
  public DerivedSchemaEntry getDerivedSchema(String storeName, int valueSchemaId, int writeComputeSchemaId) {
    requireKnownStore(storeName);
    if (valueSchemaId != VALUE_SCHEMA_ID || writeComputeSchemaId != WRITE_COMPUTE_PROTOCOL_VERSION) {
      throw new VeniceException(
          "Unknown derived schema id (" + valueSchemaId + "," + writeComputeSchemaId + ") for store: " + storeName);
    }
    return writeComputeSchemaEntry;
  }

  @Override
  public Collection<DerivedSchemaEntry> getDerivedSchemas(String storeName) {
    requireKnownStore(storeName);
    return Collections.singletonList(writeComputeSchemaEntry);
  }

  @Override
  public DerivedSchemaEntry getLatestDerivedSchema(String storeName, int valueSchemaId) {
    requireKnownStore(storeName);
    if (valueSchemaId != VALUE_SCHEMA_ID) {
      throw new VeniceException(
          "Unknown value schema id " + valueSchemaId + " for derived schema lookup on store: " + storeName);
    }
    return writeComputeSchemaEntry;
  }

  @Override
  public RmdSchemaEntry getReplicationMetadataSchema(
      String storeName,
      int valueSchemaId,
      int replicationMetadataVersionId) {
    requireKnownStore(storeName);
    if (valueSchemaId != VALUE_SCHEMA_ID || replicationMetadataVersionId != RMD_PROTOCOL_VERSION) {
      throw new VeniceException(
          "Unknown RMD schema id (" + valueSchemaId + "," + replicationMetadataVersionId + ") for store: " + storeName);
    }
    return rmdSchemaEntry;
  }

  @Override
  public Collection<RmdSchemaEntry> getReplicationMetadataSchemas(String storeName) {
    requireKnownStore(storeName);
    return Collections.singletonList(rmdSchemaEntry);
  }

  @Override
  public void refresh() {
    // No-op: schemas are static / pre-loaded at construction.
  }

  @Override
  public void clear() {
    // No-op: schemas are immutable for the lifetime of this repository.
  }
}
